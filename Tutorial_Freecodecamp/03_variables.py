# variables and data types
# variables are containers containing the values assigned to them
# python is not strongly typed.
# character_name = 'John'
character_name = "Tom"
character_age = 35
print("There once was a man name " + character_name)
print("He was "+ str(character_age) + " years old.")
character_name = "Mike"
print("He really liked the name " + character_name)
print("but didn't like being " + str(character_age) + ".")

# string (stores plain text)
string_name = "string"
print(type(string_name))

# integer
number_sample = 5
print(type(number_sample))

# float
float_sample = 5.5
print(type(float_sample))

# boolean True or False
is_male = True
